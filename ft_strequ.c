#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	if (!s1 || !s2)
		return (-2147483648);
	while (*s1 && *s1 == *s2)
	{
		s1++;
		s2++;
	}
	return ((*s1 - *s2 == 0) ? 1 : 0);
}
