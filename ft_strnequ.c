#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t	i;

	i = 0;
	if (!s1 || !s2)
		return (-2147483648);
	while (i < n && s1[i] && s1[i] == s2[i])
		i++;
	return ((i == n) ? 1 : (s1[i] == s2[i]));
}
