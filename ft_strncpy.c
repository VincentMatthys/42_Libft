#include "libft.h"

char	*ft_strncpy(char *dst, char const *src, size_t n)
{
	size_t	i;

	i = 0;
	while (src[i] && i < n)
	{
		dst[i] = src[i];
		i++;
	}
	if (i != n)
	{
		while (dst[i] && i < n)
		{
			dst[i] = 0;
			i++;
		}
	}
	return (dst);
}
